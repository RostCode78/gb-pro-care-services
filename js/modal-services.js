const pMS = {
    openModal: $$('.box-service'),
    modal: $('.modal-services-container'),
    closeModal: $('.close-modal-services')
}

const mMS = {

    start: () => {

        pMS.openModal.forEach( e => {
            e.addEventListener(
                'click',
                mMS.openModal
            )
        });

        pMS.closeModal.addEventListener(
            'click',
            mMS.closeModal
        );

        pMS.modal.addEventListener(
            'click',
            mMS.closeModal
        );

    },

    openModal: e => {

        pMS.modal.style.display = 'flex';

        if ( e.target == pMS.openModal[0] ) {
            pMS.modal.innerHTML = modalServices01;
        } else if ( e.target == pMS.openModal[1] ) {
            pMS.modal.innerHTML = modalServices02;
        } else if ( e.target == pMS.openModal[2] ) {
            pMS.modal.innerHTML = modalServices03;
        } else if ( e.target == pMS.openModal[3] ) {
            pMS.modal.innerHTML = modalServices04;
        } else if ( e.target == pMS.openModal[4] ) {
            pMS.modal.innerHTML = modalServices05;
        } else if ( e.target == pMS.openModal[5] ) {
            pMS.modal.innerHTML = modalServices06;
        } else {
            pMS.modal.innerHTML = modalServices01;
        }

    },

    closeModal: e => {

        pMS.modal.style.display = 'none';

    }

}

mMS.start();